// Simple Auth Gate
class AuthGate extends StatefulWidget {
  static const routeName = '/auth-gate'; // Define routeName for AuthGate
  const AuthGate({super.key});

  @override
  State<AuthGate> createState() => _AuthGateState();
}

class _AuthGateState extends State<AuthGate> {
  final UserService _userService = UserService();

  @override
  Widget build(BuildContext context) {
    return StreamBuilder<AuthState>(
      stream: Supabase.instance.client.auth.onAuthStateChange,
      builder: (context, snapshot) {
        // Important: Supabase client initializes session from localStorage asynchronously.
        // The stream will initially emit the current state (potentially null if session not loaded yet)
        // and then emit again once the session is loaded from storage or if it changes.

        if (snapshot.connectionState == ConnectionState.waiting) {
          // This might be shown briefly while the stream is connecting.
          // Or if Supabase is still trying to load the initial session.
          print("AuthGate: Stream connection waiting.");
          return const Scaffold(
            body: Center(
              child: CircularProgressIndicator(
                key: ValueKey("AuthGateStreamWaiting"),
              ),
            ),
          );
        }

        if (snapshot.hasError) {
          print("AuthGate: Stream error: ${snapshot.error}");
          // Handle error, perhaps navigate to login or show error message
          WidgetsBinding.instance.addPostFrameCallback((_) {
            if (mounted &&
                ModalRoute.of(context)?.settings.name !=
                    LoginScreen.routeName) {
              Navigator.pushReplacementNamed(context, LoginScreen.routeName);
            }
          });
          return const Scaffold(
            body: Center(child: Text("Auth Error. Redirecting...")),
          );
        }

        final authState = snapshot.data;
        final user = authState?.session?.user;

        if (user != null) {
          // User is logged in (or session restored)
          // Now fetch profile and navigate
          print(
            "AuthGate: User session available (User ID: ${user.id}). Fetching profile.",
          );
          return FutureBuilder<app_profile.Profile?>(
            future: _userService.getUserProfile(user.id),
            builder: (context, profileSnapshot) {
              if (profileSnapshot.connectionState == ConnectionState.waiting) {
                print("AuthGate: Fetching profile...");
                return const Scaffold(
                  body: Center(
                    child: CircularProgressIndicator(
                      key: ValueKey("AuthGateProfileLoading"),
                    ),
                  ),
                );
              }

              if (profileSnapshot.hasError ||
                  !profileSnapshot.hasData ||
                  profileSnapshot.data == null) {
                print(
                  "AuthGate: Error fetching profile or profile is null. Error: ${profileSnapshot.error}",
                );
                // Profile fetch failed or no profile, sign out and redirect to login
                WidgetsBinding.instance.addPostFrameCallback((_) async {
                  if (mounted) {
                    await Supabase.instance.client.auth
                        .signOut(); // Ensure clean logout
                    if (ModalRoute.of(context)?.settings.name !=
                        LoginScreen.routeName) {
                      Navigator.pushReplacementNamed(
                        context,
                        LoginScreen.routeName,
                      );
                    }
                  }
                });
                return const Scaffold(
                  body: Center(child: Text("Profile error. Redirecting...")),
                );
              }

              final profile = profileSnapshot.data!;
              // Update UserProfileProvider after build only if profile changed
              WidgetsBinding.instance.addPostFrameCallback((_) {
                if (mounted) {
                  final userProfileProvider = Provider.of<UserProfileProvider>(context, listen: false);
                  if (userProfileProvider.profile?.id != profile.id) {
                    userProfileProvider.setProfile(profile);
                  }
                }
              });

              // Perform navigation after build
              WidgetsBinding.instance.addPostFrameCallback((_) {
                if (!mounted) return;

                // Normalize role defensively and add hard override for known worker account
                final rawRole = (profile.role).toString();
                final role = rawRole.trim().toLowerCase();
                final email = (profile.email ?? '').trim().toLowerCase();
                final isKnownWorker = email == 'worker@example.com';

                String effectiveRole = role;
                if (isKnownWorker && (role.isEmpty || !(role == 'admin' || role == 'driver' || role == 'worker'))) {
                  effectiveRole = 'worker';
                }

                switch (effectiveRole) {
                  case 'admin':
                    print('[AuthGate] Admin user detected. Email: $email');
                    
                    // Initialize push notifications for admin only (youssef@gmail.com)
                    if (email == 'youssef@gmail.com') {
                      print('[AuthGate] Initializing push notifications for admin...');
                      Future.microtask(() async {
                        try {
                          print('[AuthGate] Calling PushNotificationService.initialize()...');
                          await PushNotificationService().initialize();
                          print('INFO: Push notification service initialized for admin.');
                        } catch (e) {
                          print('WARNING: Push notification service initialization failed: $e');
                          print('ERROR STACK: ${StackTrace.current}');
                        }
                      });
                    } else {
                      print('[AuthGate] Not youssef@gmail.com, skipping push notifications. Email was: $email');
                    }
                    
                    if (ModalRoute.of(context)?.settings.name != MainScreen.routeName) {
                      Navigator.pushReplacementNamed(context, MainScreen.routeName);
                    }
                    break;
                  case 'driver':
                    if (ModalRoute.of(context)?.settings.name != DriverAppShell.routeName) {
                      Navigator.pushReplacementNamed(context, DriverAppShell.routeName);
                    }
                    break;
                  case 'worker':
                    if (ModalRoute.of(context)?.settings.name != WorkerAppShell.routeName) {
                      Navigator.pushReplacementNamed(context, WorkerAppShell.routeName);
                    }
                    break;
                  default:
                    // Unknown role: attempt to self-heal for known worker via Edge Function, else sign out
                    if (isKnownWorker) {
                      () async {
                        try {
                          final user = Supabase.instance.client.auth.currentUser;
                          if (user != null) {
                            await Supabase.instance.client.functions.invoke(
                              'ensure-worker-profile',
                              body: {
                                'userId': user.id,
                                'email': user.email,
                                'fullName': 'Kitchen Worker',
                              },
                            );
                          }
                        } catch (_) {}
                        if (!mounted) return;
                        if (ModalRoute.of(context)?.settings.name != WorkerAppShell.routeName) {
                          Navigator.pushReplacementNamed(context, WorkerAppShell.routeName);
                        }
                      }();
                    } else {
                      Supabase.instance.client.auth.signOut();
                      if (ModalRoute.of(context)?.settings.name != LoginScreen.routeName) {
                        Navigator.pushReplacementNamed(context, LoginScreen.routeName);
                      }
                    }
                    break;
                }
              });
              // Return a loading indicator while navigation is scheduled
              return const Scaffold(
                body: Center(

                  child: CircularProgressIndicator(
                    key: ValueKey("AuthGateNavigating"),
                  ),
                ),
              );
            },
          );
        } else {
          // No user session: go to the unified Login screen
          WidgetsBinding.instance.addPostFrameCallback((_) {
            if (mounted &&
                ModalRoute.of(context)?.settings.name !=
                    LoginScreen.routeName) {
              Navigator.pushReplacementNamed(context, LoginScreen.routeName);
            }
          });
          return const Scaffold(
            body: Center(child: CircularProgressIndicator()),
          );
        }
      },
    );

}
}


// Very Basic Login Screen (for development/testing) - This can likely be removed if LoginScreen is robust
class BasicLoginScreen extends StatefulWidget {
  static const routeName = '/basic-login'; // Added routeName
  const BasicLoginScreen({super.key});

  @override
  State<BasicLoginScreen> createState() => _BasicLoginScreenState();
}

class _BasicLoginScreenState extends State<BasicLoginScreen> {
  final _formKey = GlobalKey<FormState>();
  final _emailController = TextEditingController();
  final _passwordController = TextEditingController();
  bool _isLoading = false;

  Future<void> _signIn() async {
    if (!_formKey.currentState!.validate()) return;

    setState(() => _isLoading = true);
    try {
      final AuthResponse res = await Supabase.instance.client.auth
          .signInWithPassword(
            email: _emailController.text.trim(),
            password: _passwordController.text.trim(),
          );
      // AuthGate's onAuthStateChange listener will handle redirection and profile fetching.
      if (res.user != null) {
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
              content: Text('Signed in successfully! Redirecting...'),
              backgroundColor: Colors.green,
            ),
          );
        }
      } else {
        if (mounted) {
          _showCopyableError(context, 'Sign in failed. Check credentials.');
        }
      }
    } on AuthException catch (e) {
      if (mounted) {
        _showCopyableError(context, 'Sign in error: ${e.message}');
      }
    } catch (e) {
      if (mounted) {
        _showCopyableError(context, 'An unexpected error occurred: $e');
      }
    } finally {
      if (mounted) setState(() => _isLoading = false);
    }
  }

  void _showCopyableError(BuildContext context, String message) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Row(
          children: [
            const Icon(Icons.error_outline, color: Colors.white, size: 20),
            const SizedBox(width: 8),
            Expanded(child: Text(message, maxLines: 3, overflow: TextOverflow.ellipsis)),
          ],
        ),
        backgroundColor: Colors.red[700],
        duration: const Duration(seconds: 8),
        behavior: SnackBarBehavior.floating,
        action: SnackBarAction(
          label: '📋 COPY',
          textColor: Colors.white,
          onPressed: () {
            Clipboard.setData(ClipboardData(text: message));
            ScaffoldMessenger.of(context).showSnackBar(
              const SnackBar(
                content: Text('✓ Error copied to clipboard'),
                backgroundColor: Colors.green,
                duration: Duration(seconds: 2),
              ),
            );
          },
        ),
      ),
    );
  }

  @override
  void dispose() {
    _emailController.dispose();
    _passwordController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('Login (Dev)')),
      body: Center(
        child: SingleChildScrollView(
          padding: const EdgeInsets.all(24.0),
          child: Form(
            key: _formKey,
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                Text(
                  'Dev Login',
                  style: Theme.of(context).textTheme.headlineSmall,
                ),
                const SizedBox(height: 20),
                TextFormField(
                  controller: _emailController,
                  decoration: const InputDecoration(
                    labelText: 'Email',
                    border: OutlineInputBorder(),
                  ),
                  validator: (value) =>
                      value == null || value.isEmpty ? 'Enter email' : null,
                  keyboardType: TextInputType.emailAddress,
                ),
                const SizedBox(height: 12),
                TextFormField(
                  controller: _passwordController,
                  decoration: const InputDecoration(
                    labelText: 'Password',
                    border: OutlineInputBorder(),
                  ),
                  obscureText: true,
                  validator: (value) =>
                      value == null || value.isEmpty ? 'Enter password' : null,
                ),
                const SizedBox(height: 20),
                _isLoading
                    ? const CircularProgressIndicator()
                    : ElevatedButton(
                        onPressed: _signIn,
                        style: ElevatedButton.styleFrom(
                          minimumSize: const Size(double.infinity, 50),
                        ),
                        child: const Text('Sign In'),
                      ),
                const SizedBox(height: 20),
                TextButton(
                  onPressed: () async {
                    // Example: Sign up a test user if one doesn't exist
                    // In a real app, you'd have a separate sign-up screen
                    try {
                      await Supabase.instance.client.auth.signUp(
                        email: 'test@example.com', // Use a test email
                        password: 'password', // Use a test password
                      );
                      if (!context.mounted) return;
                      ScaffoldMessenger.of(context).showSnackBar(
                        const SnackBar(
                          content: Text(
                            'Test user sign up initiated. Check email for confirmation if enabled, or try logging in if auto-confirm is on.',
                          ),
                        ),
                      );
                    } catch (e) {
                      if (mounted) {
                        ScaffoldMessenger.of(context).showSnackBar(
                          SnackBar(
                            content: Text('Test user sign up error: $e'),
                          ),
                        );
                      }
                    }
                  },
                  child: const Text(
                    '(Dev: Sign Up Test User test@example.com / password)',
                  ),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
