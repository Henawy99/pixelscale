bool _parseBoolFlag(String? v, {bool defaultValue = false}) {
  if (v == null || v.isEmpty) return defaultValue;
  final s = v.toLowerCase().trim();
  return s == 'true' || s == '1' || s == 'yes' || s == 'y';
}


Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // Start the local scan server on non-web platforms
  if (!kIsWeb) {
    // Global listener: push ReceiptScannerScreen when an image arrives
    LocalScanServer().onImageReceived = (bytes) {
      final nav = appNavigatorKey.currentState;
      if (nav == null) return;
      // If we’re already on the scanner, just let that screen handle it.
      final currentRoute = ModalRoute.of(nav.context)?.settings.name ?? '';
      if (currentRoute == '/receipt-scanner') {
        return;
      }
      nav.push(
        MaterialPageRoute(
          settings: const RouteSettings(name: '/receipt-scanner'),
          builder: (_) => ReceiptScannerScreen(initialImageBytes: bytes),
        ),
      );
    };

    await LocalScanServer().startServer();
  }

  try {
    // Use local variables that can be updated by .env if needed on mobile
    String supabaseUrl = supabaseUrlFromEnv;
    String supabaseAnonKey = supabaseAnonKeyFromEnv;
    // String geminiApiKey = geminiApiKeyFromEnv; // For Gemini API Key

    // Listener flags (defaults: order off, purchase on). Can be overridden by --dart-define or .env
    bool enableOrderListener = _parseBoolFlag(enableOrderListenerFromEnv, defaultValue: false);
    bool enablePurchaseListener = _parseBoolFlag(enablePurchaseListenerFromEnv, defaultValue: true);

    // Initialize Firebase
    // Ensure firebase_options.dart is generated by running `flutterfire configure`
    // and uncomment the import at the top of this file.
    try {
      await Firebase.initializeApp(
        options: DefaultFirebaseOptions
            .currentPlatform, // Uncomment after firebase_options.dart is generated
      );
      print('INFO: Firebase initialized successfully.');
      
      // Register background message handler
      FirebaseMessaging.onBackgroundMessage(firebaseMessagingBackgroundHandler);
    } catch (e) {
      print('WARNING: Firebase initialization failed. Error: $e');
      // Depending on how critical Firebase is at startup, you might want to handle this error.
      // For App Distribution, the app needs to build, but full Firebase functionality might not be needed for the app to start.
    }

    // If compile-time variables are not present, load from .env file for any platform.
    bool dartDefineWasEmpty =
        supabaseUrlFromEnv.isEmpty || supabaseAnonKeyFromEnv.isEmpty;
    if (dartDefineWasEmpty) {
      print(
        'INFO: --dart-define values for Supabase not found. Attempting to load from .env file...',
      );
      try {
        await dotenv.load(fileName: ".env");
        supabaseUrl = dotenv.env['SUPABASE_URL'] ?? '';
        supabaseAnonKey = dotenv.env['SUPABASE_ANON_KEY'] ?? '';
        // If not provided via --dart-define, allow .env to control listeners
        if (enableOrderListenerFromEnv.isEmpty) {
          enableOrderListener = _parseBoolFlag(dotenv.env['ENABLE_GLOBAL_ORDER_LISTENER'], defaultValue: enableOrderListener);
        }
        if (enablePurchaseListenerFromEnv.isEmpty) {
          enablePurchaseListener = _parseBoolFlag(dotenv.env['ENABLE_GLOBAL_PURCHASE_LISTENER'], defaultValue: enablePurchaseListener);
        }

        if (supabaseUrl.isNotEmpty && supabaseAnonKey.isNotEmpty) {
          print('INFO: Loaded Supabase keys from .env file.');
        } else {
          print('WARNING: .env file did not provide Supabase keys.');
        }
      } catch (e) {
        print('WARNING: Could not load .env file. Error: $e');
      }
    }

    // Final check if keys are available
    if (supabaseUrl.isEmpty || supabaseAnonKey.isEmpty) {
      print(
        'CRITICAL: SUPABASE_URL or SUPABASE_ANON_KEY is not set. App may not function correctly.',
      );
      // For a production app, you might want to throw an error here or display a specific error UI.
      // runApp(ErrorApp(message: 'Supabase configuration is missing.'));
      // return; // Prevent further execution if critical config is missing
    } else {
      print('Using Supabase URL: $supabaseUrl');
      print(
        'Supabase Anon Key is ${supabaseAnonKey.isNotEmpty ? "Loaded" : "MISSING"}',
      );
    }

    // Validate Supabase configuration early to avoid runtime URI errors
    final parsedUri = Uri.tryParse(supabaseUrl);
    final hasValidUrl =
        parsedUri != null && parsedUri.hasScheme && parsedUri.host.isNotEmpty;
    if (!hasValidUrl || supabaseAnonKey.isEmpty) {
      print(
        'CRITICAL: Invalid Supabase configuration. URL or Anon Key missing/malformed.',
      );
      runApp(
        MaterialApp(
          home: Scaffold(
            body: Center(
              child: Padding(
                padding: const EdgeInsets.all(24.0),
                child: Text(
                  'Missing/invalid Supabase configuration.\n\n'
                  'Please set SUPABASE_URL and SUPABASE_ANON_KEY via --dart-define or a .env file at project root.\n\n'
                  'Example .env:\n'
                  'SUPABASE_URL=https://YOUR_PROJECT_REF.supabase.co\n'
                  'SUPABASE_ANON_KEY=YOUR_ANON_KEY',
                  textAlign: TextAlign.center,
                ),
              ),
            ),
          ),
        ),
      );
      return;
    }

    // Initialize Hive
    await Hive.initFlutter();
    // Register Adapters
    Hive.registerAdapter(MaterialItemAdapter()); // Register MaterialItemAdapter
    Hive.registerAdapter(
      InventoryLogItemAdapter(),
    ); // Register InventoryLogItemAdapter
    Hive.registerAdapter(BrandAdapter()); // Register BrandAdapter
    Hive.registerAdapter(MenuCategoryAdapter()); // Register MenuCategoryAdapter
    Hive.registerAdapter(MenuItemAdapter()); // Register MenuItemAdapter
    
    // Register worker cache model adapters
    Hive.registerAdapter(CachedBrandAdapter());
    Hive.registerAdapter(CachedCategoryAdapter());
    Hive.registerAdapter(CachedMenuItemAdapter());
    Hive.registerAdapter(CachedMaterialAdapter());
    Hive.registerAdapter(CachedMenuItemMaterialAdapter());
    Hive.registerAdapter(WorkerCacheMetadataAdapter());

    // Open any globally used boxes
    await Hive.openBox(
      'app_settings',
    ); // For storing cache timestamps or general settings
    await Hive.openBox<List<dynamic>>('menuCategoriesBox');
    await Hive.openBox<Map<String, List<dynamic>>>('menuItemsBox');

    await Supabase.initialize(
      url: supabaseUrl, // Use value from String.fromEnvironment
      anonKey: supabaseAnonKey, // Use value from String.fromEnvironment
    );
    
    // Note: Push notifications will be initialized after login for admin users only
    
    runApp(
      MultiProvider(
        providers: [
          ChangeNotifierProvider(create: (context) => CartProvider()),
          ChangeNotifierProvider(create: (context) => UserProfileProvider()),
        ],
        child: RestaurantManagerApp(
          enableOrderListener: enableOrderListener,
          enablePurchaseListener: enablePurchaseListener,
        ),
      ),
    );
  } catch (e) {
    print('CRITICAL: Error during app initialization: $e');
    // In a real app, show a user-friendly error screen
    runApp(
      MaterialApp(
        home: Scaffold(body: Center(child: Text('Initialization Error: $e'))),
      ),
    );
  }
}

